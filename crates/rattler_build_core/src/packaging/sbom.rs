    #[test]
    fn test_sbom_bom_ref_collision_disambiguated() {
        let mut output = curl_output();
        let subdir = output.build_configuration.target_platform.to_string();
        let name = output.name().clone();
        let version = output.version().clone();
        let build = output.build_string().into_owned();
        let primary_purl = conda_purl(
            name.as_normalized(),
            &version.to_string(),
            &build,
            None,
            &subdir,
        );

        // a resolved host record identical to the primary with no derivable
        // channel produces the same purl and would collide with the primary
        let finalized = output.finalized_dependencies.as_mut().unwrap();
        let mut record = finalized.build.as_ref().unwrap().resolved[0].clone();
        record.channel = None;
        record.package_record.name = name;
        record.package_record.version = version;
        record.package_record.build = build;
        record.package_record.subdir = subdir;
        finalized.host = Some(ResolvedDependencies {
            specs: vec![],
            resolved: vec![record],
        });

        let bom = generate_sbom(&output, &[]).unwrap();

        // the colliding record keeps its purl but gets a distinct bom-ref
        let resolved_ref = format!("{primary_purl}#resolved");
        let component = bom
            .components
            .as_ref()
            .unwrap()
            .0
            .iter()
            .find(|component| component.bom_ref.as_deref() == Some(resolved_ref.as_str()))
            .expect("colliding record gets a disambiguated bom-ref");
        assert_eq!(
            component.purl.as_ref().map(|purl| purl.to_string()),
            Some(primary_purl.clone())
        );

        // the primary keeps the plain purl as its bom-ref, so both are unique
        let primary = bom.metadata.unwrap().component.unwrap();
        assert_eq!(primary.bom_ref.as_deref(), Some(primary_purl.as_str()));

        // the dependency graph references both the primary and the resolved ref
        let refs: Vec<String> = bom
            .dependencies
            .unwrap()
            .0
            .iter()
            .map(|dependency| dependency.dependency_ref.clone())
            .collect();
        assert!(refs.contains(&resolved_ref));
        assert!(refs.contains(&primary_purl));
    }

    #[test]
    fn test_sbom_includes_cache_environments() {
        let mut output = curl_output();
        let template = output
            .finalized_dependencies
            .as_ref()
            .unwrap()
            .build
            .as_ref()
            .unwrap()
            .resolved[0]
            .clone();

        let mut cache_host = template.clone();
        cache_host.package_record.name = "cachehostpkg".parse().unwrap();
        let mut cache_build = template;
        cache_build.package_record.name = "cachebuildpkg".parse().unwrap();

        output.finalized_cache_dependencies = Some(FinalizedDependencies {
            build: Some(ResolvedDependencies {
                specs: vec![],
                resolved: vec![cache_build],
            }),
            host: Some(ResolvedDependencies {
                specs: vec![],
                resolved: vec![cache_host],
            }),
            run: FinalizedRunDependencies {
                depends: vec![],
                constraints: vec![],
                extra_depends: Default::default(),
                run_exports: Default::default(),
            },
        });

        let bom = generate_sbom(&output, &[]).unwrap();
        let components = bom.components.unwrap().0;
        let find = |name: &str| {
            components
                .iter()
                .find(|component| component.name.to_string() == name)
                .unwrap_or_else(|| panic!("component {name} is present"))
        };

        let host = find("cachehostpkg");
        assert_eq!(host.scope, Some(Scope::Required));
        assert!(property_values(host).contains(&(
            "rattler-build:environment".to_string(),
            "cache-host".to_string()
        )));

        let build = find("cachebuildpkg");
        assert_eq!(build.scope, Some(Scope::Excluded));
        assert!(property_values(build).contains(&(
            "rattler-build:environment".to_string(),
            "cache-build".to_string()
        )));
    }

    #[test]
    fn test_sbom_dependency_and_mapped_purl_properties() {
        let mut output = curl_output();

        let depends = source_spec("foo >=1.0");
        let constrains = source_spec("bar <2.0");
        let expected_depends = depends.spec().to_string();
        let expected_constrains = constrains.spec().to_string();

        let finalized = output.finalized_dependencies.as_mut().unwrap();
        finalized.run.depends = vec![depends];
        finalized.run.constraints = vec![constrains];

        // attach a mapped purl to the first build record
        let mapped: PackageUrl = "pkg:pypi/foo@1.2.3".parse().unwrap();
        let record = &mut finalized.build.as_mut().unwrap().resolved[0];
        record.package_record.purls = Some(BTreeSet::from([mapped]));
        let mapped_record = record.package_record.name.as_normalized().to_string();

        let bom = generate_sbom(&output, &[]).unwrap();

        let primary = bom.metadata.as_ref().unwrap().component.as_ref().unwrap();
        let primary_properties = property_values(primary);
        assert!(
            primary_properties.contains(&("rattler-build:depends".to_string(), expected_depends))
        );
        assert!(
            primary_properties
                .contains(&("rattler-build:constrains".to_string(), expected_constrains))
        );

        let component = bom
            .components
            .as_ref()
            .unwrap()
            .0
            .iter()
            .find(|component| component.name.to_string() == mapped_record)
            .expect("mapped record is present");
        assert!(property_values(component).contains(&(
            "rattler-build:mapped-purl".to_string(),
            "pkg:pypi/foo@1.2.3".to_string()
        )));
    }
