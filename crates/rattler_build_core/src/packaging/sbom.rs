/// Name of the SBOM document generated by rattler-build inside `info/sboms/`.
pub const GENERATED_SBOM_FILE_NAME: &str = "rattler-build.cdx.json";

/// A file collected from the build's SBOM directory.
struct CollectedSbom {
    /// File name inside `info/sboms/`.
    file_name: String,
    /// BOM-Link when the file identifies itself as a CycloneDX document.
    bom_link: Option<String>,
}

/// A resolved record together with the environments it appears in.
struct EnvironmentRecord<'a> {
    record: &'a RepoDataRecord,
    /// The record's purl, used as the component's `purl` field. The bom-ref may
    /// differ from this when it would collide with the primary component.
    purl: String,
    /// Environment labels the record appears in, e.g. `host`, `build`,
    /// `cache-host` and `cache-build`. One property is emitted per label.
    environments: BTreeSet<&'static str>,
    /// Whether the record appears in a host-like environment. Required scope
    /// wins over excluded when a record appears in both.
    required: bool,
}

/// Deterministic serial number so identical builds produce identical SBOMs.
fn serial_number(output: &Output) -> UrnUuid {
    let namespace = Uuid::new_v5(&Uuid::NAMESPACE_DNS, b"rattler.build");
    let identifier = format!(
        "{}/{}-{}",
        output.build_configuration.target_platform,
        output.identifier(),
        output.build_configuration.timestamp.as_second()
    );
    UrnUuid::from(Uuid::new_v5(&namespace, identifier.as_bytes()))
}

/// Build a `pkg:conda` purl string. Qualifier values are percent encoded and
/// emitted in the alphabetical key order mandated by the purl spec.
fn conda_purl(
    name: &str,
    version: &str,
    build: &str,
    channel: Option<&str>,
    subdir: &str,
) -> String {
    let mut purl = format!(
        "pkg:conda/{name}@{}?build={}",
        urlencoding::encode(version),
        urlencoding::encode(build)
    );
    if let Some(channel) = channel {
        purl.push_str("&channel=");
        purl.push_str(&urlencoding::encode(channel));
    }
    purl.push_str("&subdir=");
    purl.push_str(&urlencoding::encode(subdir));
    purl
}

/// Derive a channel name from a channel URL: all non-empty path segments,
/// joined with '/', after stripping a trailing subdir segment. This keeps the
/// label path of label channels intact (e.g. `conda-forge/label/rust_dev`).
fn channel_short_name(channel: &str, subdir: &str) -> Option<String> {
    match Url::parse(channel) {
        Ok(url) => {
            let mut segments: Vec<_> = url.path_segments()?.filter(|s| !s.is_empty()).collect();
            if segments.last() == Some(&subdir) {
                segments.pop();
            }
            if segments.is_empty() {
                None
            } else {
                Some(segments.join("/"))
            }
        }
        // channels can also be configured as plain names
        Err(_) if !channel.is_empty() && !channel.contains('/') => Some(channel.to_string()),
        Err(_) => None,
    }
}

/// The purl of a resolved record, the basis for its bom-ref.
fn record_purl(record: &RepoDataRecord) -> String {
    let package = &record.package_record;
    let channel = record
        .channel
        .as_deref()
        .and_then(|channel| channel_short_name(channel, &package.subdir));
    conda_purl(
        package.name.as_normalized(),
        &package.version.to_string(),
        &package.build,
        channel.as_deref(),
        &package.subdir,
    )
}

/// Emit an SPDX expression license when the string parses as one, a named
/// license otherwise.
fn license_choice(license: &str) -> LicenseChoice {
    if spdx::Expression::parse(license).is_ok() {
        LicenseChoice::expression(license)
    } else {
        LicenseChoice::license(license)
    }
}

/// Sort properties by name and value for reproducible output.
fn sorted_properties(mut properties: Vec<Property>) -> Option<Properties> {
    if properties.is_empty() {
        return None;
    }
    properties.sort_by(|a, b| (&a.name, &*a.value).cmp(&(&b.name, &*b.value)));
    Some(Properties(properties))
}

/// Best effort peek into a collected JSON document. Returns a BOM-Link when
/// the top level object carries a `serialNumber` urn:uuid string and a
/// positive integer `version`.
fn peek_bom_link(content: &str) -> Option<String> {
    let value: serde_json::Value = serde_json::from_str(content).ok()?;
    let serial = value.get("serialNumber")?.as_str()?;
    let version = value.get("version")?.as_u64()?;
    if version == 0 {
        return None;
    }
    let uuid = Uuid::parse_str(serial.strip_prefix("urn:uuid:")?).ok()?;
    Some(format!("urn:cdx:{uuid}/{version}"))
}

/// Generate the CycloneDX document describing the output: the package itself,
/// the resolved host, build and staging cache environments, the run dependency
/// specs and references to the collected SBOM files.
fn generate_sbom(output: &Output, collected: &[CollectedSbom]) -> Result<Bom, PackagingError> {
    let finalized = output.finalized_dependencies.as_ref();
    let host = finalized.and_then(|dependencies| dependencies.host.as_ref());
    let build = finalized.and_then(|dependencies| dependencies.build.as_ref());
    let cache = output.finalized_cache_dependencies.as_ref();
    let cache_host = cache.and_then(|dependencies| dependencies.host.as_ref());
    let cache_build = cache.and_then(|dependencies| dependencies.build.as_ref());

    // primary component identity, also used to detect bom-ref collisions
    let name = output.name().as_normalized();
    let version = output.version().to_string();
    let subdir = output.build_configuration.target_platform.to_string();
    let primary_purl = conda_purl(name, &version, &output.build_string(), None, &subdir);

    // a record whose purl equals the primary's purl needs a distinct bom-ref;
    // bom-refs are opaque strings, so a suffix keeps it unique and deterministic
    // without touching the record's own purl
    let bom_ref = |purl: &str| -> String {
        if purl == primary_purl {
            format!("{purl}#resolved")
        } else {
            purl.to_string()
        }
    };

    // one component per unique record across every environment, keyed by bom-ref
    let mut records: BTreeMap<String, EnvironmentRecord> = BTreeMap::new();
    // the records resolved from the direct specs of every environment
    let mut primary_depends = BTreeSet::new();
    // for every record, its `depends` entries resolved by name within the
    // environment(s) it appears in
    let mut record_depends: BTreeMap<String, BTreeSet<String>> = BTreeMap::new();

    // host-like environments contribute the required scope, build-like ones the
    // excluded scope; each contributes its own `environment` property
    let environments = [
        (host, "host", true),
        (build, "build", false),
        (cache_host, "cache-host", true),
        (cache_build, "cache-build", false),
    ];
    for (resolved, environment, required) in environments {
        let Some(resolved) = resolved else { continue };

        let by_name: BTreeMap<&str, String> = resolved
            .resolved
            .iter()
            .map(|record| {
                (
                    record.package_record.name.as_normalized(),
                    bom_ref(&record_purl(record)),
                )
            })
            .collect();

        for spec in &resolved.specs {
            if let Some(reference) = spec
                .spec()
                .name
                .as_exact()
                .and_then(|name| by_name.get(name.as_normalized()))
            {
                primary_depends.insert(reference.clone());
            }
        }

        for record in &resolved.resolved {
            let purl = record_purl(record);
            let reference = bom_ref(&purl);

            let entry = records
                .entry(reference.clone())
                .or_insert_with(|| EnvironmentRecord {
                    record,
                    purl,
                    environments: BTreeSet::new(),
                    required: false,
                });
            entry.environments.insert(environment);
            entry.required |= required;

            let depends = record_depends.entry(reference).or_default();
            for dependency in &record.package_record.depends {
                let name = dependency.split_whitespace().next().unwrap_or(dependency);
                // names that do not resolve within the environment are
                // skipped, e.g. virtual packages
                if let Some(reference) = by_name.get(name) {
                    depends.insert(reference.clone());
                }
            }
        }
    }

    // primary component
    let mut primary = Component::new(
        Classification::Library,
        name,
        &version,
        Some(primary_purl.clone()),
    );
    primary.purl = Some(Purl::from_str(&primary_purl).expect("any string is a valid Purl value"));

    let about = output.recipe.about();
    primary.description = about.summary.as_deref().map(NormalizedString::new);
    if let Some(license) = &about.license {
        primary.licenses = Some(Licenses(vec![license_choice(&license.to_string())]));
    }

    let mut references = Vec::new();
    if let Some(homepage) = &about.homepage {
        references.push(ExternalReference::new(
            ExternalReferenceType::Website,
            Uri::new(homepage.as_str()),
        ));
    }
    if let Some(repository) = &about.repository {
        references.push(ExternalReference::new(
            ExternalReferenceType::Vcs,
            Uri::new(repository.as_str()),
        ));
    }
    for file in collected {
        let url = match &file.bom_link {
            Some(link) => ReferenceUri::BomLink(BomLink(link.clone())),
            None => ReferenceUri::Url(Uri::new(&format!(
                "./{}",
                urlencoding::encode(&file.file_name)
            ))),
        };
        references.push(ExternalReference {
            external_reference_type: ExternalReferenceType::Bom,
            url,
            comment: Some(format!(
                "{} was collected from the SBOM_DIR of the build",
                file.file_name
            )),
            hashes: None,
        });
    }
    if !references.is_empty() {
        primary.external_references = Some(ExternalReferences(references));
    }

    let mut properties = Vec::new();
    if let Some(run) = finalized.map(|dependencies| &dependencies.run) {
        for spec in run
            .depends
            .iter()
            .map(|dep| dep.spec().to_string())
            .unique()
        {
            properties.push(Property::new("rattler-build:depends", &spec));
        }
        for spec in run
            .constraints
            .iter()
            .map(|dep| dep.spec().to_string())
            .unique()
        {
            properties.push(Property::new("rattler-build:constrains", &spec));
        }
    }
    primary.properties = sorted_properties(properties);

    // environment components, sorted by bom-ref
    let mut components = Vec::new();
    for (reference, entry) in &records {
        let package = &entry.record.package_record;
        let mut component = Component::new(
            Classification::Library,
            package.name.as_normalized(),
            &package.version.to_string(),
            Some(reference.clone()),
        );
        component.purl =
            Some(Purl::from_str(&entry.purl).expect("any string is a valid Purl value"));
        component.scope = Some(if entry.required {
            Scope::Required
        } else {
            Scope::Excluded
        });

        let mut hashes = Vec::new();
        if let Some(md5) = &package.md5 {
            hashes.push(Hash {
                alg: HashAlgorithm::MD5,
                content: HashValue(hex::encode(md5)),
            });
        }
        if let Some(sha256) = &package.sha256 {
            hashes.push(Hash {
                alg: HashAlgorithm::SHA_256,
                content: HashValue(hex::encode(sha256)),
            });
        }
        if !hashes.is_empty() {
            component.hashes = Some(Hashes(hashes));
        }

        if let Some(license) = &package.license {
            component.licenses = Some(Licenses(vec![license_choice(license)]));
        }

        component.external_references = Some(ExternalReferences(vec![ExternalReference::new(
            ExternalReferenceType::Distribution,
            Uri::new(entry.record.url.as_str()),
        )]));

        let mut properties = Vec::new();
        for environment in &entry.environments {
            properties.push(Property::new("rattler-build:environment", environment));
        }
        for mapped in package.purls.iter().flatten() {
            properties.push(Property::new(
                "rattler-build:mapped-purl",
                &mapped.to_string(),
            ));
        }
        component.properties = sorted_properties(properties);

        components.push(component);
    }

    let mut dependencies = vec![Dependency {
        dependency_ref: primary_purl,
        dependencies: primary_depends.into_iter().collect(),
    }];
    for reference in records.keys() {
        dependencies.push(Dependency {
            dependency_ref: reference.clone(),
            dependencies: record_depends
                .remove(reference)
                .unwrap_or_default()
                .into_iter()
                .collect(),
        });
    }

    let build_tool = output.system_tools.build_tool();
    let tool = Component::new(
        Classification::Application,
        &build_tool.name,
        &build_tool.version,
        None,
    );

    let timestamp = output.build_configuration.timestamp.to_string();
    let metadata = Metadata {
        timestamp: Some(
            DateTime::try_from(timestamp)
                .map_err(|err| PackagingError::SbomGenerationError(err.to_string()))?,
        ),
        tools: Some(Tools::Object {
            services: None,
            components: Some(Components(vec![tool])),
        }),
        component: Some(primary),
        ..Metadata::default()
    };

    Ok(Bom {
        version: 1,
        serial_number: Some(serial_number(output)),
        metadata: Some(metadata),
        components: Some(Components(components)),
        services: None,
        external_references: None,
        dependencies: Some(Dependencies(dependencies)),
        compositions: None,
        properties: None,
        vulnerabilities: None,
        signature: None,
        annotations: None,
        formulation: None,
        spec_version: SpecVersion::V1_5,
    })
}

/// Serialize the document as pretty printed CycloneDX 1.5 JSON with a
/// trailing newline.
fn render_sbom(bom: Bom) -> Result<Vec<u8>, PackagingError> {
    let mut buffer = Vec::new();
    bom.output_as_json_v1_5(&mut buffer)
        .map_err(|err| PackagingError::SbomGenerationError(err.to_string()))?;
    buffer.push(b'\n');
    Ok(buffer)
}

/// Validate a collected file's name: reject non-UTF-8 names, and names that
/// collide with the reserved generated document. The reserved-name comparison
/// is case-insensitive because a case variant would still overwrite the
/// generated document on case-insensitive filesystems. `path` is the full
/// source path, used for error reporting.
fn collected_file_name(os_file_name: &OsStr, path: &Path) -> Result<String, PackagingError> {
    let file_name = os_file_name
        .to_str()
        .ok_or_else(|| PackagingError::SbomNonUtf8FileName(path.to_path_buf()))?;
    if file_name.eq_ignore_ascii_case(GENERATED_SBOM_FILE_NAME) {
        return Err(PackagingError::SbomReservedFileName(file_name.to_string()));
    }
    Ok(file_name.to_string())
}

/// True when the path has a `json` extension, compared case-insensitively.
fn has_json_extension(path: &Path) -> bool {
    path.extension()
        .and_then(|extension| extension.to_str())
        .is_some_and(|extension| extension.eq_ignore_ascii_case("json"))
}

/// Copy the files from the build's SBOM directory into `info/sboms/` and
/// write the generated CycloneDX document next to them. Returns the paths of
/// all written files.
pub(crate) fn write_sbom_folder(
    output: &Output,
    tmp_dir_path: &Path,
) -> Result<Vec<PathBuf>, PackagingError> {
    let sboms_folder = tmp_dir_path.join("info/sboms");
    fs::create_dir_all(&sboms_folder)?;

    let sbom_dir = output.build_configuration.directories.sbom_dir();
    let mut sources = Vec::new();
    if sbom_dir.is_dir() {
        for entry in fs::read_dir(&sbom_dir)? {
            let path = entry?.path();
            // only regular files are collected, no directories and no
            // recursion
            if path.is_file() {
                sources.push(path);
            }
        }
    }
    sources.sort_by_key(|path| path.file_name().map(|name| name.to_os_string()));

    let mut files = Vec::new();
    let mut collected = Vec::new();
    for path in sources {
        let os_file_name = path.file_name().expect("read_dir entries have a file name");
        let file_name = collected_file_name(os_file_name, &path)?;

        let destination = sboms_folder.join(os_file_name);
        fs::copy(&path, &destination)?;
        files.push(destination);

        let bom_link = has_json_extension(&path)
            .then(|| fs::read_to_string(&path).ok())
            .flatten()
            .and_then(|content| peek_bom_link(&content));
        collected.push(CollectedSbom {
            file_name,
            bom_link,
        });
    }

    let bom = generate_sbom(output, &collected)?;
    let generated = sboms_folder.join(GENERATED_SBOM_FILE_NAME);
    fs::write(&generated, render_sbom(bom)?)?;
    files.push(generated);

    Ok(files)
}

